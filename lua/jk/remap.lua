local y = true
vim.keymap.set("n", "<leader>cc", function()
	if y then
		vim.opt.colorcolumn = "80"
		y = false
	else
		vim.opt.colorcolumn = ""
		y = true
	end
end, {desc = "toggle colorcolumn"})


